public class Caculation {
    public static void main(String[] args) {
        System.out.println("5 + 5는 " + (5 + 5) + " 입니다.");
        System.out.println("5 - 5는 " + (5 - 5) + " 입니다.");
        System.out.println("5 * 5는 " + (5 * 5) + " 입니다.");
        System.out.println("5 / 5는 " + (5 / 5) + " 입니다.");
        System.out.println("5 % 5는 " + (5 % 5) + " 입니다.");

    }
}
